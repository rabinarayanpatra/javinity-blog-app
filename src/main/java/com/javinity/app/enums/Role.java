package com.javinity.app.enums;

/**
 * Represents the roles that can be assigned to a user within the application.
 * <p>
 * The available roles define the level of access and permissions a user has.
 * </p>
 */
public enum Role {

  /**
   * Administrative role with elevated privileges.
   */
  ADMIN,

  /**
   * Standard user role with basic access permissions.
   */
  USER
}
